#include "3-calc.h"
/**
 * op_add - add 2 numbers
 * @a: first number
 * @b: second number
 * Return: return sum
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - substract 2 numbers
 * @a: first number
 * @b: second number
 * Return: return sum
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiply 2 numbers
 * @a: first number
 * @b: second number
 * Return: return mul
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - divide 2 numbers
 * @a: first number
 * @b: second number
 * Return: return division
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - calculate the modulo
 * @a: first number
 * @b: second number
 * Return: return modulo
 */
int op_mod(int a, int b)
{
	return (a % b);
}
